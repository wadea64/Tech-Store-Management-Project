    private void setupSalesEngine() {
        txtsearch.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent evt) {
                if (txtsearch.getText().equals("Search for products by name...")) {
                    txtsearch.setText("");
                    txtsearch.setForeground(java.awt.Color.BLACK);
                }
            }
            @Override
            public void focusLost(java.awt.event.FocusEvent evt) {
                if (txtsearch.getText().isEmpty()) {
                    txtsearch.setText("Search for products by name...");
                    txtsearch.setForeground(new java.awt.Color(102, 102, 102));
                }
            }
        });

        lbldashboard.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
        lbldashboard.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                frmdashboard db = new frmdashboard();
                db.setVisible(true);
                dispose();
            }
        });

        lblinventory.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
        lblinventory.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                frminventory inv = new frminventory();
                inv.setVisible(true);
                dispose();
            }
        });

        lbllogout.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
        lbllogout.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int confirm = javax.swing.JOptionPane.showConfirmDialog(frmsalesprocceisng.this,
                    "Are you sure you want to logout?", "Logout", javax.swing.JOptionPane.YES_NO_OPTION);
                if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                    frmlogin login = new frmlogin();
                    login.setVisible(true);
                    dispose();
                }
            }
        });

        btnfinish.addActionListener(new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                processCheckout();
            }
        });
    }

    private void processCheckout() {
        if (jLabel1.getText().isEmpty() && jLabel2.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "The checkout cart is currently empty!", "Checkout Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        double totalToCharge = 0.0;
        StringBuilder receiptDetails = new StringBuilder("=== TRANSACTION RECEIPT ===\n\n");

        if (!jLabel1.getText().isEmpty()) {
            frmdashboard.addLiveSale(1600.0, "RTX 4090 GPU");
            totalToCharge += 1600.0;
            receiptDetails.append("- RTX 4090 GPU: $1600.00\n");
        }
        if (!jLabel2.getText().isEmpty()) {
            frmdashboard.addLiveSale(50.0, "Wireless Mouse");
            totalToCharge += 50.0;
            receiptDetails.append("- Wireless Mouse: $50.00\n");
        }

        receiptDetails.append("\n-----------------------------\n");
        receiptDetails.append(String.format("TOTAL PAID: $%.2f\n", totalToCharge));
        receiptDetails.append("=============================\n");
        receiptDetails.append("Payment Processed Successfully!");

        javax.swing.JOptionPane.showMessageDialog(this, receiptDetails.toString(), "Receipt Printer Terminal", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        
        jLabel1.setText("");
        jLabel2.setText("");
        lbltotal.setText("Total: $0.00");
    }
    
