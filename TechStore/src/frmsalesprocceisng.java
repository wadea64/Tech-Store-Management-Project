    private void txtsearchActionPerformed(java.awt.event.ActionEvent evt) {                                          
        String query = txtsearch.getText().toLowerCase().trim();
        if (query.contains("gpu") || query.contains("4090")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Product Found: RTX 4090 GPU\nPrice: $1600\nStatus: In Cart", "Search Result", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else if (query.contains("mouse") || query.contains("wireless")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Product Found: Wireless Mouse\nPrice: $50\nStatus: In Cart", "Search Result", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Product not found in system.", "Search Result", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }                                         

