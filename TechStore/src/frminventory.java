    private void handleRowAction(javax.swing.JLabel nameLbl, javax.swing.JLabel priceLbl, javax.swing.JLabel qtyLbl) {
        String[] options = {"Edit Stock/Price", "Delete Product", "Cancel"};
        int choice = javax.swing.JOptionPane.showOptionDialog(this, 
            "What action do you want to perform on " + nameLbl.getText() + "?", 
            "Product Actions", javax.swing.JOptionPane.DEFAULT_OPTION, 
            javax.swing.JOptionPane.QUESTION_MESSAGE, null, options, options[0]);

        if (choice == 0) { 
            String newPrice = javax.swing.JOptionPane.showInputDialog(this, "Enter New Price ($):", priceLbl.getText().replace("$", ""));
            String newQty = javax.swing.JOptionPane.showInputDialog(this, "Enter New Stock Quantity:", qtyLbl.getText());
            
            try {
                double p = Double.parseDouble(newPrice);
                int q = Integer.parseInt(newQty);
                if (p < 0 || q < 0) throw new NumberFormatException();
                
                priceLbl.setText(String.format("%.0f$", p));
                qtyLbl.setText(String.valueOf(q));
                
               
                if (q <= 5) {
                    qtyLbl.setForeground(java.awt.Color.RED);
                } else {
                    qtyLbl.setForeground(java.awt.Color.BLACK);
                }
                
                javax.swing.JOptionPane.showMessageDialog(this, "Product information updated successfully!");
                syncWithDashboard();
                
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Invalid numeric input. Changes discarded.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } else if (choice == 1) { 
            int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to clear this item slot?", "Confirm Delete", javax.swing.JOptionPane.YES_NO_OPTION);
            if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                nameLbl.setText("[Empty Slot]");
                priceLbl.setText("0$");
                qtyLbl.setText("0");
                qtyLbl.setForeground(java.awt.Color.GRAY);
                syncWithDashboard();
            }
        }
    }

    private void syncWithDashboard() {
        int activeProducts = 0;
        int lowStockTotal = 0;

        
        if (!lbltheproductname.getText().equals("[Empty Slot]")) {
            activeProducts++;
            int q1 = Integer.parseInt(lblthestockquantity.getText());
            if (q1 <= 5 && q1 > 0) lowStockTotal++;
        }
        
        if (!lbltheproductname1.getText().equals("[Empty Slot]")) {
            activeProducts++;
            int q2 = Integer.parseInt(lblthestockquantity1.getText());
            if (q2 <= 5 && q2 > 0) lowStockTotal++;
        }

        
        if (frmdashboard.totalProducts == 0 && activeProducts > 0) {
            frmdashboard.totalProducts = 128; 
            frmdashboard.lowStockCount = 3;
        } else {
            frmdashboard.totalProducts = activeProducts;
            frmdashboard.lowStockCount = lowStockTotal;
        }
    }
   
