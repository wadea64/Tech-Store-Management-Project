    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String name = javax.swing.JOptionPane.showInputDialog(this, "Enter Product Name:");
        if (name == null || name.trim().isEmpty()) return;

        String category = javax.swing.JOptionPane.showInputDialog(this, "Enter Category:");
        if (category == null || category.trim().isEmpty()) return;

        String priceStr = javax.swing.JOptionPane.showInputDialog(this, "Enter Price ($):");
        if (priceStr == null) return;

        String qtyStr = javax.swing.JOptionPane.showInputDialog(this, "Enter Stock Quantity:");
        if (qtyStr == null) return;

        
        try {
            double price = Double.parseDouble(priceStr);
            int qty = Integer.parseInt(qtyStr);

            if (price < 0 || qty < 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Values cannot be negative!", "Validation Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            
            javax.swing.JOptionPane.showMessageDialog(this, "Product '" + name + "' added successfully!\n(UI rows full, system counters updated).");
            
            frmdashboard.totalProducts += 1;
            if (qty <= 5) frmdashboard.lowStockCount += 1;
            
            syncWithDashboard();

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid Format! Price must be a decimal number and Stock must be a whole integer.", "Validation Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }                                        

